# функции в Python

def function(a):
    if a == 1:
        return 'Единица'
    elif a > 1:
        return 13
    else:
        return

arg = 1
print(function(arg))
print(type(function(arg)))

arg_2 = 4
print(function(arg_2))
print(type(function(arg_2)))

arg_3 = -6
print(function(arg_3))
print(type(function(arg_3)))